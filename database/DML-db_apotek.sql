-- Check apakah data sudah masuk semua
SELECT 
    t.name AS Table_Name,
    SUM(p.rows) AS Jumlah_Data
FROM sys.tables t
JOIN sys.partitions p 
    ON t.object_id = p.object_id
WHERE p.index_id IN (0, 1)
GROUP BY t.name
ORDER BY Jumlah_Data DESC;
